class Conditional
{
	public static void main(String[] args)
	{
		int i = 30;
		
		switch(i)
		{
			case 10: System.out.println("i = 10");
			break;
			case 20: System.out.println("i = 20");
			break;
			case 30: System.out.println("i = 30");
			break;
			case 40: System.out.println("i = 40");
			break;
			case 50: System.out.println("i = 50");
			break;
			case 60: System.out.println("i = 60");
			break;
			case 70: System.out.println("i = 70");
			break;
			case 80: System.out.println("i = 80");
			break;
			case 90: System.out.println("i = 90");
			break;
			case 100: System.out.println("i = 100");
			break;

		}
	}
}